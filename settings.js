// Функция поиска по модели
function findSettings(model) {
  const clean = model.toLowerCase().trim();

  // Точное совпадение
  if (SETTINGS[clean]) return SETTINGS[clean];

  // Поиск по вхождению
  for (const [key, val] of Object.entries(SETTINGS)) {
    if (clean.includes(key) || key.includes(clean)) return val;
  }

  // Поиск по бренду + части
  for (const [key, val] of Object.entries(SETTINGS)) {
    const keyParts = key.split(' ');
    const matchCount = keyParts.filter(kp => clean.includes(kp)).length;
    if (matchCount >= 2) return val;
  }

  return null;
}

